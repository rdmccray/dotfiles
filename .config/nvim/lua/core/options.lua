local opt = vim.opt

-- line numbers
opt.relativenumber = true
opt.number = true
opt.scrolloff = 10

-- tabs and indentation
opt.tabstop = 2
opt.shiftwidth = 2
opt.expandtab = true
opt.autoindent = true

-- line wrapping
opt.wrap = false

-- search settings
opt.ignorecase = true
opt.smartcase = true

-- cursor line
opt.cursorline = true

-- appearance
opt.termguicolors = true
opt.background = "dark"
opt.signcolumn = "yes"

-- backspace
opt.backspace = "indent,eol,start"

-- clipboard
opt.clipboard:append("unnamedplus")

-- split Windows
opt.splitright = true
opt.splitbelow = true

-- consider - as part of keyword
opt.iskeyword:append("-")

-- disable the mouse while in nvim
opt.mouse = ""

-- file folding
opt.foldlevel = 20
opt.foldmethod = "expr"
opt.foldexpr = "nvim_treesitter#foldexpr()" -- Utilize Treesitter folds
